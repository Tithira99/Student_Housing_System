    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       if(dlttxt.getText().equals("")){
           statustxt.setText("Enter Apartment ID to Delete");
       }
       else{
        boolean elementfound=false;
        for(int i=0;i<alist.size();i++){
            if(alist.get(i).getid().equals(dlttxt.getText())){
            statustxt.setText("the Element "+alist.get(i).getid()+" has been deleted");
            alist.remove(i);
             elementfound=true;
             dlttxt.setText("");
             break;
            }
        }
        
        
        
        if(elementfound==false){
            statustxt.setText("Element not found");
        }
        
       
       setlist();
       }
         
    }//GEN-LAST:event_jButton2ActionPerformed

