    private void InsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InsertActionPerformed
        
     try{
        if("".equals(idtxt.getText())||"".equals(loctxt.getText())||"".equals(nroomstxt.getValue())||"".equals(Avlcmb.getSelectedItem())||"".equals(renttxt.getText())||"".equals(qstxt.getText())){
        statustxt.setText("Any of attributes cannot be empty");
        }
        else if(Integer.parseInt(qstxt.getText())>100||Integer.parseInt(qstxt.getText())<0){
            statustxt.setText("Quality Score shoulbe within 0 to 100");
        }
        else{
            boolean Eavailable=false;
        for(int i=0;i<alist.size();i++){
            if(alist.get(i).getid().equals(idtxt.getText())){
                Eavailable=true;
            }
         } 
        
        if(Eavailable != true){
        Apartment ap=new Apartment(idtxt.getText(),loctxt.getText(),Integer.parseInt(nroomstxt.getValue().toString()),Boolean.parseBoolean((String) Avlcmb.getSelectedItem()),Integer.parseInt(renttxt.getText()), Integer.parseInt(qstxt.getText()));
        
        alist.add(ap);
        statustxt.setText("Apartment has been added");
        }else{
             statustxt.setText("Use a different ID!");
        }
         
       
        setlist();
        
        idtxt.setText("");
        loctxt.setText("");
        Avlcmb.setSelectedItem(true);
        renttxt.setText("");
        qstxt.setText("");
        
          
      
       
        }
     }catch(Exception ex){
         statustxt.setText(ex.toString());
     }
    }//GEN-LAST:event_InsertActionPerformed
   
        
    
   
